    fetchImageData: async function (scriptUrl) {
        try {
            const cacheKey = 'gallery-images-cache';
            const cacheTime = 'gallery-images-cache-time';
            const now = Date.now();
            const thirtyMinutes = 30 * 60 * 1000;
            const cachedData = localStorage.getItem(cacheKey);
            const cacheTimestamp = localStorage.getItem(cacheTime);
            if (cachedData && cacheTimestamp && (now - parseInt(cacheTimestamp)) < thirtyMinutes) {
                return JSON.parse(cachedData)
            }
            const response = await this.fetchWithTimeout(scriptUrl, {
                method: 'GET',
                headers: {
                    'Accept': 'text/plain; charset=utf-8',
                }
            }, 15000);
            if (!response.ok) {
                throw new Error('Error al cargar datos: ' + response.status)
            }
            const textData = await response.text();
            const lines = textData.split('\n').filter(line => line.trim() !== '');
            const imageData = [];
            for (const line of lines) {
                const parts = line.split('|');
                if (parts.length >= 5) {
                    imageData.push({
                        id: parseInt(parts[0]) || Date.now() + Math.random(),
                        title: parts[1] || '',
                        description: parts[2] || '',
                        thumbnail: parts[3] || '',
                        fullSize: parts[4] || ''
                    })
                }
            }
            localStorage.setItem(cacheKey, JSON.stringify(imageData));
            localStorage.setItem(cacheTime, now.toString());
            return imageData
        } catch (error) {
            console.error('Error fetching image data:', error);
            return this.getFallbackImageData()
        }
    },
